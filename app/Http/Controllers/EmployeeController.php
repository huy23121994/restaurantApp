     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $employee = Employee::find($id);
        if ($employee) {
            return view('app.employees.show', compact('employee'));
        }else{
            abort('404_user');
        }
    }

